bool checkKorCode(byte hi, byte lo)
{
	// hi: xx
	// lo: yy
	if ((0xA1 > lo) || (0xFE < lo)) {
		return false;
	}
	if ((hi >= 0xB0) && (hi <= 0xC8)) {
		return true;
	}
	return false;
}

