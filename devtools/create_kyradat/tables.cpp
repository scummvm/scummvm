ExtractEntryList getProvidersForId(int id) {
	ExtractEntryList list;

	for (const ExtractEntry *p = extractProviders; p->id != -1; ++p) {
		if (p->id == id) {
			for (const ExtractEntrySearchData *d = p->providers; d->hint.size != 0; ++d)
				list.push_back(*d);
		}
	}

	return list;
}
